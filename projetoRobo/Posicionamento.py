class Posicionamento(object):
	
	def __init__(x, y, ori):
		eixoX = x
		eixoY = y
		orientacao = ori
	
	def getEixoX():
		return eixoX
	
	def getEixoY():
		return eixoY
	
	def getOrientacao():
		return orientacao
	
	def setEixoX(x):
		eixoX = x
	
	def setEixoY(y):
		eixoY = y
	
	def setOrientacao(ori):
		orientacao = ori
